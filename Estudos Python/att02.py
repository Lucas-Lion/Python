n = input('Digite algo: ')

print('tipo primitivo: ', type(n))
print('é alfabético? ', n.isalpha())
print('é alfanumérico? ', n.isalnum())
print('é numérico? ', n.isnumeric())
print('é maisculo? ', n.isupper())
print('é espaçado? ', n.isspace())


